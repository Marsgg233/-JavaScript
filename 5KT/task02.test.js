const { getInfo } = require('./task02');

describe('getInfo', () => {
  test('1', () => { //возвращает правильное количество
    const prices = [   
      'Цена товара - 1200$',   
      'Стоимость - 500$',   
      'Цена не определена', 
      '9999',    
      'Ценовая категория - больше 300$',    
      'Цена за услугу 500 EUR',    
      '150$', 
    ];

    expect(getInfo(prices)).toEqual([3, 4]);
  });
});